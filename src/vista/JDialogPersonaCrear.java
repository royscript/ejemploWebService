    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        int id = Integer.parseInt(this.txtId.getText());
        String rut = this.txtRut.getText();
        String nombres = this.txtNombres.getText();
        String secNombre = this.txtSecNombre.getText();
        String apPaterno = this.txtApPaterno.getText();
        String apMaterno = this.txtApMaterno.getText();
        Persona persona = new Persona(id,rut,nombres,secNombre,apPaterno,apMaterno);
        try {
            persona.ingresar();
        } catch (IOException ex) {
            Logger.getLogger(JDialogPersonaCrear.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_botonGuardarActionPerformed

